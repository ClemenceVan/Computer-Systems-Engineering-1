double average_magnitude() {
    double sum = 0;
    for (int i = 0; i < 100; i++)
        sum += magnitude(nbr[i]);
    return sum / 100;
}

double minimum_magnitude() {
    double min = magnitude(nbr[0]);
    for (int i = 1; i < 100; i++)
        if (magnitude(nbr[i]) < min)
            min = magnitude(nbr[i]);
    return min;
}

double maximum_magnitude() {
    double max = magnitude(nbr[0]);
    for (int i = 1; i < 100; i++)
        if (magnitude(nbr[i]) > max)
            max = magnitude(nbr[i]);
    return max;
}

double variance_magnitude() {
    double avg = average_magnitude();
    double sum = 0;
    for (int i = 0; i < 100; i++)
        sum += pow(magnitude(nbr[i]) - avg, 2);
    return sum / 100;
}

